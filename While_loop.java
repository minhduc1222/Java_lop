public class While_loop {
    public static void main(String[] arg){
        //while loop
        int i = 0;
        while(i < 5){
            System.out.println(i);
            i++;
        }
        //do loop
        int j = 0;
        do{
            System.out.println(j);
            j++;
        }
        while (j < 5);
    }
    
}
